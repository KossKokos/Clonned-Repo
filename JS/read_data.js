const fs = require("fs");

//read shoes list file

function getShoesFile(shoesListFP) {
  return new Promise((resolve, reject) => {
    fs.readFile(shoesListFP, "utf-8", (err, data) => {
      if (err) {
        reject(`Error: ${err}`);
      } else {
        resolve(JSON.parse(data));
      }
    });
  });
}

